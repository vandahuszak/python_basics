#Round a number to given a precision in decimal digits
print(round(8 / 3))

#localizing
print(round(2.6666666666, 2))

# /= += it is a shortcut: reference to the variable
result = 4 / 2
result /= 2
print(result)

score = 0
#User scores a point
score += 1
print(score)

